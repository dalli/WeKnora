def translate_text(text):
    """간단한 번역 함수 - 실제로는 더 정교한 번역이 필요"""
    # 문자열 리터럴만 번역
    if text.startswith('"') and text.endswith('"'):
        content = text[1:-1]
        # 주요 키워드 번역
        for cn, ko in translations.items():
            content = content.replace(cn, ko)
        return f'"{content}"'
    return text

